Estas ◊em{grasas monoinsaturadas} nos protegen de infartos porque aumentan ◊link["es-malo-el-colesterol.html"]{◊em{las lipoproteínas HDL}} que, como vimos antes, limpian la grasa excesiva de las arterias y evitan que se obstruyan (entonces también evitan ◊link["infartos-y-obesidad.html"]{◊em{los infartos}}).  
