◊margin-note{¿Comer pocos carbohidratos y más grasas y proteínas nos ayuda a adelgazar?}

◊indented{◊image[#:width "100%" #:border #f "atkins-proteina.jpg"]}

Así que, como los carbohidratos son los alimentos que más suben la glucosa de la sangre, se propuso que una dieta baja en carbohidratos puede disminuir la insulina y esto nos ayudaría a adelgazar.

Como vimos antes, cuando comemos carbohidratos aumenta mucho la glucosa de la sangre. Entonces para reducir y normalizar estos niveles, el páncreas produce ◊link["que-hace-la-insulina"]{insulina}, una hormona que lleva el exceso de glucosa fuera de la circulación, hacia el hígado donde se almacenará para usos futuros o hacia el tejido adiposo donde se almacenará como grasa. Y así engordamos (Ver ¿◊xref{que hace la insulina}?)

En cambio las proteínas y las grasas elevan muy poco la glucosa de la sangre. Así que, tendría sentido  pensar que una dieta baja en carbohidratos, pero con más proteínas y grasas, nos ayudaría a bajar de peso. Pero esto no demostró ser así.