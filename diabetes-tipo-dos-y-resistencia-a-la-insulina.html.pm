◊hanging-topic[(topic-from-metas metas)]{La resistencia a la insulina está presente en la obesidad y la diabetes tipo dos}

Esta relación entre ◊em{el exceso de peso y la diabetes tipo dos} se explica porque ◊xref{la resistencia a la insulina} está presente muy frecuentemente en la obesidad y siempre en la diabetes tipo dos. 

Por eso también se le conoce, a la diabetes tipo dos, como ◊em{diabetes insulinorresistente}, pues en este caso, el cuerpo se vuelve resistente a los efectos de la insulina (Ver ¿◊xref{que hace la insulina}?). Y, aunque el páncreas de los diabéticos tipo dos produce mucha hormona, esta insulina no logra bajar los altos niveles de glucosa de la sangre.

Por eso podríamos decir que la diabetes tipo dos es igual a mucha insulina y mucha glucosa en la sangre:

