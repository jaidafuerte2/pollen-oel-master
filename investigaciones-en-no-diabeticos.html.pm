Entonces podríamos pensar que la insulina solo engorda a diabéticos. Pero no es así, por ejemplo, hay fármacos que usan personas ◊em{sin diabetes}, pero como efecto secundario, aumentan la insulina y engordan.

◊margin-note{◊image[#:width "70%" "no-diabeticos.jpg"]}

Es el caso de la ◊em{olanzapina y la quetiapina}, que se usan para tratar trastornos psiquiátricos. Pero como efecto secundario producen un aumento de peso de ◊em{1 o 2 kilos}.