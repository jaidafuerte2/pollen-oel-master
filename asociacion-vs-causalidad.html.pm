◊hanging-topic[(topic-from-metas metas)]{Una asociación no demuestra causalidad}

El problema es que en epidemiología ◊em{una asociación no demuestra causalidad}. Es decir que haya una asociación entre comer grasas y los infartos, no demuestra que las grasas causan infartos, porque ◊em{la asociación y la causalidad} son conceptos distintos.

Para entender esto podemos hacer una analogía. Pensemos en un incendio al que llegan muchos bomberos. Obviamente los bomberos no son la causa del incendio, son sólo la respuesta al incendio para apagarlo.

