◊(define-meta title "frutas y fructosa")
◊hanging-topic[(topic-from-metas metas)]{Las frutas tienen poca fructosa}

Esta capacidad de la fructosa para llenar al hígado de grasa nos haría pensar que comer ◊em{frutas} puede causar ◊em{hígado graso}.

◊gap[2]

◊margin-note{◊image[#:border #t #:width "90%" "frutas-ok.jpg"]}

Pero esto no es así porque las ◊em{frutas} aportan pequeñas cantidades de ◊em{fructosa} a la dieta, sólo ◊em{15 o 20 gramos} diarios, y esto no nos afecta.

En cambio el azúcar -y los alimentos y bebidas que se preparan con mucho azúcar- tienen un montón de fructosa. Por eso es muy importante dejar el azúcar y los productos azucarados si queremos adelgazar.
