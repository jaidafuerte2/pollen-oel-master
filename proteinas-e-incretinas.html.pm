◊(define-meta title "proteinas e incretinas")
◊hanging-topic[(topic-from-metas metas)]{estimulan la producción de insulina}

Como vimos anteriormente, ◊xref{las incretinas} son unas hormonas que se producen en el intestino cada vez que comemos alimentos como: grasas, proteínas o carbohidratos. Y una de las funciones de estas hormonas es estimular al páncreas para que produzca ◊xref{la hormona insulina} (la hormona que aumenta nuestras reservas de grasa). Y así engordamos.

Por ejemplo consumir ciertas proteínas animales, como la proteína del suero de la leche, estimulan la producción de incretinas en un 298%. Por eso ◊em{los lácteos} elevan mucho los niveles de insulina.

◊indented{◊image[#:width "90%" #:border #f "lacteos-incretinas.jpg"]}

Sin embargo esto no quiere decir que inevitablemente todas las proteínas engordan porque ◊xref{las incretinas} no solo estimulan la producción de insulina, también generan la ◊em{sensación de saciedad}.

Esto pasa porque las incretinas vuelven lento el paso de alimentos del estómago al intestino, lo que hace que la comida se quede más tiempo en el estómago y así nos sentimos más llenos.

Así que para saber si las proteínas nos engordan debemos preguntarnos cuál de los dos efectos de ◊xref{las incretinas} es más potente:

◊margin-note{◊image[#:width "90%" #:border #f "gordo-flaco.jpg"]}

◊numbered-list{
El ◊em{efecto engordador} de la insulina o


El efecto ◊em{adelgazante} que se produce por la sensación de saciedad.}

Y para contestar esta pregunta debemos considerar a cada proteína por separado porque cada una tiene un efecto distinto sobre el peso. En este sentido las dos proteínas más estudiadas son la ◊xref{proteina de la carne} y la ◊xref{proteina de la leche}.