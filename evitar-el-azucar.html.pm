◊(define-meta title "evitar el azucar")
◊hanging-topic[(topic-from-metas metas)]{es muy importante para adelgazar}

Es importante evitar ◊em{el azúcar, la miel y la panela} -no solo- porque son carbohidratos procesados sin fibra, sino también porque ◊em{la sacarosa} (que contienen) causa ◊link["introduccion-al-higado-graso.html"]{◊em{hígado graso}} lo que estimula al páncreas a producir mucha insulina extra (la hormona que aumenta nuestras reservas de grasa. Ver ◊xref{introduccion al higado graso}).

◊indented{◊image[#:width "70%" #:border #f "higado-graso.jpg"]}

Recordemos que la sacarosa contiene mucha ◊em{fructosa} que, cuando se metaboliza en el hígado, produce grasas que se quedan dentro de este órgano, ocupando espacio que normalmente usa (el hígado) para guardar el exceso de glucosa de la sangre.

Pero este exceso de glucosa no puede quedarse en la circulación porque es tóxico. Así que para forzar la entrada de –al menos- un poco de glucosa a un hígado lleno de grasa, el páncreas produce insulina extra y engordamos.

