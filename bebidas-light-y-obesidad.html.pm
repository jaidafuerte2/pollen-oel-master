◊hanging-topic[(topic-from-metas metas)]{Las bebidas light no han mostrado beneficios}

A pesar de esto, no solo en las investigaciones les va mal a los ◊em{edulcorantes}. Además, desde una perspectiva histórica, el consumo de edulcorantes y bebidas light aumentó un ◊em{400% desde 1960 hasta el año 2000}.

Sin embargo, durante ese mismo período de tiempo, también aumentó  mucho la obesidad. Es decir, los edulcorantes no han demostrado beneficios.

