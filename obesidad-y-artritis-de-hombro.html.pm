◊hanging-topic[(topic-from-metas metas)]{La obesidad causa artritis de hombros y muñecas}

Sin embargo en la obesidad también es frecuente ◊xref{la inflamacion} de articulaciones que no soportan demasiado peso como ◊em{las muñecas y los hombros}. Esto se explica porque el ◊em{exceso de tejido graso} genera una ◊em{respuesta inflamatoria aumentada} que se caracteriza por producir muchos glóbulos blancos y citoquinas, principalmente:

◊numbered-list{
Leptina,


Adiponectina y 


Resistina.}

Estas son sustancias que dañan el cartílago porque también limitan la actividad reparadora de los condrocitos, impidiendo que formen colágeno y proteoglicanos (necesarios para que se forme y regenere el cartílago).
