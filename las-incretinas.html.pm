◊hanging-topic[(topic-from-metas metas)]{Las incretinas estimulan la producción de insulina}

Las ◊em{incretinas} son hormonas que se producen en el intestino cuando comemos cualquier tipo de alimento como grasas, proteínas o carbohidratos.

Y estas ◊em{incretinas} estimulan al páncreas para que produzca insulina (y recordemos que ◊xref{la hormona insulina} aumenta nuestras reservas de grasa y nos engorda).

Sin embargo las grasas nos engordan menos que los carbohidratos porque no elevan la glucosa de la sangre (entonces el cuerpo no necesita insulina extra para disminuir y normalizar la glucosa de la sangre -Ver ¿◊xref{que hace la insulina}?-).  

Además, las grasas quitan el apetito porque las ◊em{incretinas} generan hormonas de la saciedad, como: ◊em{Colecistoquinina, Péptido YY y Amilina}. Esto puede ayudarnos a que tengamos menos hambre, comamos menos y adelgacemos.

◊indented{◊image[#:width "70%" #:border #f "pan-mantequilla.jpg"]}

En términos prácticos esto quiere decir que si comemos una galleta con mantequilla, lo que más nos engorda es la harina de la galleta, no la mantequilla.
