La obesidad causa muchas enfermedades porque el exceso de grasa en nuestro cuerpo altera el funcionamiento de nuestros órganos y tejidos. Por eso ahora sabemos que la obesidad, no solo, cambia la figura de nuestro cuerpo sino que es un problema de salud real. Diap2

Durante mucho tiempo las personas e incluso la comunidad médica pensaron que la obesidad solo era un problema estético. Sin embargo ahora, luego de muchas investigaciones, sabemos que el exceso de peso  está asociado a muchas enfermedades. Diap 3

De hecho, ahora es ampliamente aceptado entre médicos y otros expertos que la obesidad está asociada a enfermedades como diabetes, hipertensión, hígado graso, ovario poliquístico, artritis, ataques de corazón, derrames cerebrales, entre otras (*1). Diap 4

Pero ¿Por qué la obesidad y el exceso de grasa en nuestro cuerpo logran dañar tan eficientemente nuestros órganos y tejidos? Eso es lo que explicaremos en esta serie de publicaciones, aunque podemos adelantar algunos puntos.   

Cuando hablamos de la obesidad inmediatamente pensamos en lo más evidente: la gordura, los kilos extras y los rollitos en el abdomen. Pero detrás de los aspectos estéticos, están los problemas que no podemos ver con facilidad. Los verdaderos malhechores están escondidos tras el escenario. Diap 5

Los dos grandes agresores en esta historia son el exceso de insulina y la inflamación que produce la obesidad. La insulina es la hormona encargada de aumentar nuestras reservas de grasa pero tiene también otras funciones. Tener demasiada insulina altera estas funciones.

Por otro lado tenemos el estado de inflamación que produce la obesidad. Cuando tenemos exceso de grasa, el propio tejido graso del cuerpo produce sustancias que causan inflamación y son muy peligrosas para nuestra salud, produciendo incluso infartos. Diap 6

Toda gran historia tiene un agresor. Así, en los cuentos maravillosos hay una bruja que representa el lado oscuro del bosque y quiere aprovecharse de sus víctimas. Diap 7

Así también, la obesidad tiene un lado oscuro representado por el exceso de insulina y el estado inflamatorio que produce el exceso de grasa. 

De esta forma, en las próximas publicaciones hablaremos sobre la forma en que estos dos agresores nos causan enfermedades como diabetes, hipertensión, artritis, infartos, hígado graso, entre otras. 

Esperamos que nuestra audiencia disfrute leyendo estas publicaciones tanto como nosotros disfrutamos escribiéndolas.         
