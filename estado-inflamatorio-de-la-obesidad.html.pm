◊(define-meta title "estado inflamatorio de la obesidad")
◊hanging-topic[(topic-from-metas metas)]{una condición que se produce por tener demasiado tejido graso}

Adicionalmente el exceso de tejido graso  de una persona obesa genera una condición conocida como ◊link["inflamacion-y-obesidad.html"]{estado inflamatorio de bajo grado}.

Este estado se produce porque el exceso de grasa corporal produce sustancias inflamatorias que pueden lesionar órganos y tejidos del cuerpo como las arterias del corazón o el cartílago de las articulaciones, lo que puede causar ◊link["infartos-y-obesidad.html"]{infartos} o ◊link["artritis-y-obesidad.html"]{artritis}.
