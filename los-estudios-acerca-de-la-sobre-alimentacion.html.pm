◊(define-meta title "los estudios acerca de la sobre alimentacion")
◊hanging-topic[(topic-from-metas metas)]{Comer más de lo habitual no es tan fácil}

Por ejemplo, a finales de los años 70, el doctor Ethan Sims realizó varios estudios acerca de la sobrealimentación. El primer estudio lo realizó en estudiantes de la Universidad de Vermont a quienes se les animó a comer todo lo que quisieran para que aumente su ingesta calórica y engorden.

Sin embargo no llegaron a subir el peso esperado pues comer más de lo habitual no es tan fácil como se piensa porque cuando comemos demasiado:

◊numbered-list{
               Disminuyen las hormonas del hambre principalmente ◊em{la grelina}.


               Y aumentan las hormonas de la saciedad sobre todo ◊em{la colecistoquinina, el péptido YY y la amilina}.}

Esto evita que sigamos comiendo en exceso.

A Pesar de esto el doctor Sims pensó que los estudiantes estaban haciendo ejercicio y esto evitó que engorden. Así que decidió hacer una investigación similar, pero esta vez, en reclusos de la prisión estatal de Vermont,  a quienes se les podía limitar el ejercicio.

Adicionalmente a los participantes reclusos se les sobrealimentó con una dieta de ◊em{4000 calorías diarias}. Al inicio muchos estuvieron complacidos de todo lo que podían comer. Pero con el pasar del tiempo fue cada vez más difícil seguir comiendo en exceso y muchos abandonaron el estudio.
