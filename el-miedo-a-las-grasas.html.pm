◊indented{◊image[#:width "90%" #:border #f "aceite-de-oliva.jpg"]}

Todo lo que explicamos en la sección sobre ◊xref{las grasas de nuestro cuerpo} es muy importante porque durante mucho tiempo existió el mito de que comer ◊em{colesterol y grasas} eleva el colesterol de nuestra sangre y este inevitablemente tapa las arterias y causa infartos.

Sin embargo esto no es del todo cierto porque -de hecho- muchas ◊em{grasas} son saludables y evitan que las arterias se obstruyan.

Además,  ahora sabemos que las arterias no solo se obstruyen por el colesterol, sino –más bien- por una respuesta inflamatoria aumentada como la que hay en la obesidad (Ver ◊xref{exceso de inflamacion e infartos}).

Pero esto no siempre se supo. De hecho varias investigaciones –bien- concluyeron que comer ◊em{grasas} aumenta el colesterol de la sangre y esto se ◊em{◊strong{asocia}} con más riesgo de infartos (Por favor, notar que la palabra ◊em{◊strong{asocia}} está enfatizada).
