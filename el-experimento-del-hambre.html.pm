◊(define-meta title "el experimento del hambre")
◊hanging-topic[(topic-from-metas metas)]{una investigación hecha en Minnesota sobre reducir calorías}

Esto ya se sabía desde hace varias décadas. De hecho entre 1944 y 1945 se realizó una investigación muy importante sobre reducir calorías que se denominó ◊em{el experimento del hambre de Minnesota}.

En este estudio participaron 36 hombres jóvenes y saludables a los que durante 6 meses se les dio una dieta baja en calorías. Con sólo 1500 o incluso menos de 1000 calorías diarias, para bajar 24% del peso total inicial.

Al final de los 6 meses, el metabolismo de los participantes disminuyó:

◊numbered-list{
               La frecuencia cardíaca pasó de 55 a 35 latidos/minuto (en promedio).

               
               La temperatura  bajó a 35,4 grados centígrados (en promedio).
               

               La fuerza muscular se redujo un 21% (en promedio).
               

               La presión arterial disminuyó.
               

               El pelo se les cayó y las uñas se debilitaron.} 
