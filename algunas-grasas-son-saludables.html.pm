◊hanging-topic[(topic-from-metas metas)]{porque son antiinflamatorias}

Sin embargo durante décadas existió el mito de que las grasas son los alimentos que más engordan y hay que evitarlas para tener una buena salud, lo cual es falso pues -de hecho- varias grasas son saludables porque tienen efectos antiinflamatorios.

Por ejemplo, las grasas poliinsaturadas con ◊em{ácidos grasos omega 3} tienen efectos antiinflamatorios y por eso evitan que las arterias se obstruyan (entonces también evitan ◊link["infartos-y-obesidad.html"]{◊em{los infartos}}. Ver ◊xref{exceso de inflamacion e infartos}).

