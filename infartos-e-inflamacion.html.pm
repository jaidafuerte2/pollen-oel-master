◊hanging-topic[(topic-from-metas metas)]{Los infartos se producen por una respuesta inflamatoria excesiva}

Un ◊em{infarto cardíaco o cerebral} se produce cuando deja de pasar sangre a estos órganos porque una arteria se tapa,  aparentemente con mucho colesterol.

◊indented{◊image[#:width "55%" #:border #f "arteria-colesterol.jpg"]}

Sin embargo, el colesterol de la sangre no es la única razón de los ◊em{infartos} ni es la causa más importante.

De hecho, actualmente se conoce que una arteria se obstruye por una ◊link["inflamacion-y-obesidad.html"]{◊em{respuesta inflamatoria excesiva}} como la que hay en la obesidad.
