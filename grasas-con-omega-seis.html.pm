◊(define-meta title "grasas con omega seis")
◊hanging-topic[(topic-from-metas metas)]{tienen efectos inflamatorios}

Sin embargo no todas las grasas son buenas. Algunas son saludables sólo si no se comen en exceso. Este es el caso de las grasas poliinsaturadas con ◊em{ácidos grasos omega 6} presentes en los aceites de cocina de girasol, maíz y soya.

◊indented{◊image[#:width "60%" #:border #f "aceite-cocina.jpg"]}

Sucede que el maíz, la soya y el girasol contienen poca cantidad de grasa y aceite. Así que históricamente aportaron poca cantidad de ◊em{ácidos grasos omega 6} a la dieta. 

Sin embargo actualmente las industrias pueden procesar toneladas de maíz, soya o girasol para extraer grandes cantidades de aceite con una gran cantidad de ◊em{ácidos grasos omega 6}, para los cuales no estamos preparados ni evolutiva ni biológicamente pues tienen efectos inflamatorios y, si se comen en exceso, aumentan el riesgo de infartos (Ver ◊xref{exceso de inflamacion e infartos}).

◊margin-note{◊image[#:width "100%" #:border #f "aceite-maiz.jpg"]}

En términos prácticos esto significa que usar un poco de aceite de cocina para que la comida no se pegue al sartén, no nos matará. Pero no debemos comer estas grasas en exceso porque no son tan saludables.