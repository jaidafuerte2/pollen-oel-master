◊margin-note{En realidad, la inflamación crónica causa que las arterias se obstruyan y las personas se infarten.}

◊indented{◊image[#:width "80%" #:border #f "muerte.jpg"]}

Todo lo que vimos en el capítulo anterior sobre ◊xref{las grasas de nuestro cuerpo} es muy importante porque durante mucho tiempo se pensó que comer grasas aumenta el colesterol de la sangre. Y se creía que este colesterol se acumulaba en las arterias, obstruía el paso de sangre y  causaba infartos.

A simple vista esto tiene sentido porque si observamos dentro de una arteria tapada, veremos un montón de colesterol acumulado.

◊indented{◊image[#:width "50%" #:border #t "arteria-colesterol.jpg"]}

Sin embargo ahora sabemos que no es el colesterol el que inicia este problema, sino que una arteria se obstruye por una ◊em{respuesta inflamatoria aumentada} como la que hay en la obesidad.