◊hanging-topic[(topic-from-metas metas)]{Esta dieta toma su nombre por el doctor Robert Atkins}

Esta ◊em{dieta Atkins} toma su nombre por el doctor ◊em{Robert Atkins}, un cardiólogo en Nueva York que en 1963 pudo bajar de peso siguiendo las indicaciones de algunos médicos que aconsejaban una dieta baja en carbohidratos para adelgazar.

Así que, Atkins no fue el primero en crear una ◊em{dieta baja en carbohidratos}, pero fue quien la popularizó. De hecho publicó varios libros al respecto y vendió millones de copias. 

Esta dieta demostró sus beneficios a corto plazo. En investigaciones que duraban menos de un año, la dieta Atkins demostró que ayudaba a ◊em{bajar de peso}, además mejoró ◊em{la presión arterial, el colesterol y la glucosa en sangre}.

Pero a largo plazo no demostró beneficios. Las investigaciones mostraron que los participantes que seguían la ◊em{dieta Atkins} volvían a engordar luego de ◊em{12 meses} de seguir esta dieta. 

Esto sucedió porque los niveles de insulina dependen de varios factores no sólo de los carbohidratos que comemos.
