Georgia was designed primarily to work well on screen. The compromise is that it can be clunky on the printed page. ◊xref{Miller} shares Georgia’s DNA but it’s more subtle and detailed, making it preferable to Georgia for print use. (See a comparison of the two in ◊xref{system fonts}.) 

◊xref{Equity}, ◊xref{Harriet}, and ◊xref{Ingeborg} are likewise based on historical models but with a contemporary spin, making them a comfortable blend of traditional and modern.