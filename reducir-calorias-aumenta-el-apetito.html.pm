◊(define-meta title "reducir calorias aumenta el apetito")
◊hanging-topic[(topic-from-metas metas)]{y terminamos comiendo más que antes}

Además, cuando comemos menos calorías de lo habitual, también ◊em{aumenta el apetito} porque el deseo de comida se controla por mecanismos hormonales y cerebrales que se alteran cuando ingerimos menos comida de lo habitual.

Esto se demostró en una investigación que se realizó en el año 2011 cuyo objetivo era conocer los cambios hormonales durante la pérdida de peso. A los participantes de este estudio se les indicó una dieta  baja en calorías con solo 500 calorías diarias para perder un total de 13 kilos y medio en promedio.

En estos participantes se observó que:

◊numbered-list{
               Aumentó ◊em{la grelina}: una hormona que aumenta el apetito.
               

               Y disminuyeron ◊em{la amilina, el péptido YY y la colecistoquinina}: que son hormonas de la saciedad.}

Al final los participantes volvieron a engordar porque aumentó su apetito y esto provocó que vuelvan a comer mucho.
