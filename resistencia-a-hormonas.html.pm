◊(define-meta title "resistencia a hormonas")
◊hanging-topic[(topic-from-metas metas)]{Grandes cantidades de hormonas generan resistencia}

Por ejemplo hay hormonas que se liberan sólo temprano en la mañana. Como el cortisol y la noradrenalina, que liberan mucha glucosa a la sangre, dándonos energía para la actividad diaria. En cambio, estas hormonas van disminuyendo el resto del día hasta que  llegan a su punto más bajo por la noche cuando ya no tendremos actividad. 

Esto sucede porque si tendríamos mucho cortisol y noradrenalina a toda hora, nuestro cuerpo se volvería resistente a estas hormonas y dejarían de ejercer su efecto.

Otro ejemplo es la ovulación en las mujeres, que es la liberación de un óvulo que puede ser fecundado por un espermatozoide. Este suceso se produce solo cerca al día 14 del ciclo menstrual cuando ciertas hormonas femeninas alcanzan su punto más alto.

