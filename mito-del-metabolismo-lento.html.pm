◊hanging-topic[(topic-from-metas metas)]{durante el ayuno el metabolismo se acelera}

También se cree falsamente que el ayuno vuelve lento el metabolismo, es decir que el cuerpo empieza a usar menos calorías (energía) para sus funciones básicas.

Sin embargo, desde el paleolítico, los humanos tuvimos que soportar grandes períodos de escasez y, a pesar de eso, debíamos disponer de mucha energía para conseguir alimentos.

Lo que sucede es que, como vimos antes, el cuerpo usa cierta cantidad de calorías (energía) para sus funciones básicas como la respiración, la digestión o la recuperación del cansancio muscular.  

◊indented{◊image[#:width "40%" #:border #f "funciones-basicas.jpg"]}

Si el cuerpo usa menos calorías para estas funciones, decimos que el metabolismo se vuelve lento y engordamos.

Sin embargo, ◊em{ayunar no vuelve lento el metabolismo, lo acelera}. Esto pasa porque durante el ayuno, el cuerpo produce más ◊em{noradrenalina}. Esta es una hormona que acelera el metabolismo. De hecho, durante ayunos de varios días, los niveles de noradrenalina aumentan hasta un ◊em{117%} y el gasto de calorías aumenta un ◊em{12%}, lo que ayuda a adelgazar.

Esto no significa que sean necesarios ayunos de uno o más días. Sólo explica que, incluso, los ayunos largos no afectan nuestra salud y -de hecho- son saludables. Pero pueden necesitar supervisión médica.

En cambio, ◊em{los ayunos cortos de 12 o incluso 16 horas} podrían hacerse todos los días, lo cual es muy saludable y puede ayudarnos a adelgazar y además no necesitan supervisión médica.

