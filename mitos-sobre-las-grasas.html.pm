◊hanging-topic[(topic-from-metas metas)]{Por tener muchas calorías, engordan}

Uno de los mitos más extendidos fue que ◊em{las grasas nos engordan}. Y para convencernos de esto, crearon la excusa de ◊em{las calorías}. Nos dijeron que las grasas tienen más calorías que las proteínas y los carbohidratos y por eso son los alimentos que más engordan.

◊quick-table{
             Macronutriente | Calorías
             Carbohidratos | 4 kcal/gramo
             Grasas | 9 kcal/gramo
             Proteínas | 4 kcal/gramo
             }


Sin embargo ahora sabemos que la obesidad ◊link["el-error-de-reducir-calorias.html"]{◊em{no es un problema de calorías}} sino de ◊xref{la hormona insulina}. Esta es una hormona que aumenta nuestras reservas de grasa y se produce –sobre todo- cuando comemos carbohidratos procesados (como harina blanca y azúcar) porque estos alimentos elevan mucho la glucosa de la sangre (y una de las funciones de ◊link["que-hace-la-insulina.html"]{◊em{la insulina}} es retirar el exceso tóxico de glucosa de la sangre, llevándolo hacia el tejido adiposo).

En cambio, las grasas no elevan estos niveles de glucosa, pero si pueden engordarnos porque estimulan la producción de insulina gracias al efecto ◊em{incretina}.
