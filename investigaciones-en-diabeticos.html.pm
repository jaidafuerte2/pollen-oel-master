◊hanging-topic[(topic-from-metas metas)]{La insulina engorda}

Entonces en los años 70 y 90 se realizaron 2 estudios (que duraron varios años). Uno se realizó en diabéticos tipo 2 y el otro estudio se realizó en diabéticos tipo 1 respectivamente. Y se comparó a pacientes que recibían dosis altas de insulina con pacientes que recibían dosis bajas.

Al final, los pacientes que recibían dosis altas de insulina tuvieron menos complicaciones relacionadas con la diabetes. Pero subieron en promedio ◊em{4,5 kilos} más de peso que los pacientes con dosis bajas.

Pero podríamos pensar que los diabéticos que engordaron comieron más de lo habitual, pero no fue así. El aumento de peso con insulina es independiente de la cantidad de comida.

De hecho en 1993 se realizó una investigación  en pacientes diabéticos  a los que se les redujo ◊em{300 calorías} diarias de comida y se les administró altas dosis de insulina. Al final, luego de 6 meses, los participantes ganaron más de ◊em{8 kilos} de peso a pesar de que estaban comiendo menos de lo habitual.
