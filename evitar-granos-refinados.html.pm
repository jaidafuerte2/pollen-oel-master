◊hanging-topic[(topic-from-metas metas)]{principalmente harina blanca}

Sin embargo, el azúcar no es el único ◊link["carbohidratos-procesados.html"]{◊em{carbohidrato procesado}} que engorda. Todos los ◊link["la-fibra-alimentaria.html"]{◊em{carbohidratos sin fibra}} pueden subirnos de peso. Por eso el segundo paso para bajar de peso es evitar comer granos refinados, principalmente ◊em{harina blanca}.

Así que evitaremos ◊em{el pan blanco, las tostadas, las rosquillas y las galletas (no integrales)}, además de los ◊em{fideos y tallarines}.

◊margin-note{◊image[#:width "100%" #:border #f "verduras.jpg"]}

Sin embargo no todos los carbohidratos nos suben de peso, nos engordan cuando se procesan y pierden la ◊link["la-fibra-alimentaria.html"]{◊em{fibra}}. Así que carbohidratos enteros aceptables son las verduras como ◊em{la berenjena, la col, la zanahoria, el brócoli, la coliflor, la lechuga, el pepinillo, etc}.

También podríamos usar legumbres como ◊em{fréjol, arvejas, garbanzos, chochos y habas, entre otras}.

