◊hanging-topic[(topic-from-metas metas)]{porque comer mucho y con mucha frecuencia mantiene alta la insulina permanentemente}

Algo parecido pasa con la resistencia a la insulina porque cuando comemos ◊em{mucho y con mucha frecuencia}, el páncreas produce mucha insulina permanentemente. Esto, por un lado, nos engorda. Pero también, hace que la insulina pierda su efecto y ya no puede bajar los niveles de glucosa de la sangre. 

Así que para compensar y –al menos- bajar un poco la glucosa de la sangre, el páncreas produce aún más insulina (◊em{cayendo en un círculo vicioso que se autorefuerza}) y empiezan  otros problemas.

Por un lado, tanta insulina, aumenta demasiado nuestra grasa corporal. Por eso no solo engordamos:

