◊margin-note{Resistencia a la insulina es igual a mucha glucosa y mucha insulina en la sangre}

◊indented{◊image[#:width "100%" #:border #f "resistencia-insulina.jpg"]}

Ser resistente a la insulina significa que nuestro cuerpo es resistente a los efectos de esta hormona (Ver ¿◊xref{que hace la insulina}?). Es decir que, aunque el páncreas produce mucha insulina, esta hormona no logra reducir los altos niveles de  glucosa de la sangre.