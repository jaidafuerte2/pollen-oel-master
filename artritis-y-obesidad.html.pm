◊margin-note{La obesidad afecta a las articulaciones que soportan grandes y pequeñas cargas}

◊indented{◊image[#:width "100%" #:border #f "artritis-hombro.jpg"]}

En el caso de la ◊em{artritis} (inflamación de las articulaciones), parece lógico que el exceso de peso puede inflamar algunas articulaciones que soportan grandes presiones como las ◊em{rodillas y las caderas}.

Pero en la obesidad también es más frecuente la inflamación de articulaciones que no soportan grandes cargas como las ◊em{muñecas}. 

Esto demuestra que las articulaciones no solo se inflaman por exceso de presión sino también porque en la obesidad hay un ◊em{estado inflamatorio de larga duración} que afecta al cartílago de todas las articulaciones –incluso- de las que no soportan grandes cargas como ◊em{las muñecas y los hombros}.
