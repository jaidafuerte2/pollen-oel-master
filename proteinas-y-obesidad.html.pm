◊margin-note{A pesar de que las proteínas elevan poco la glucosa de la sangre, si pueden engordarnos}

◊indented{◊image[#:width "90%" #:border #f "proteina-carnica.jpg"]}

Sin embargo (y a pesar de que grasas y proteínas generan la sensación de saciedad) esto no significa que ingerir lácteos y carnes necesariamente nos ayudará a adelgazar porque las carnes y los lácteos también nos engordan si se comen en exceso aunque elevan poco la glucosa de la sangre (entonces el cuerpo no necesita tanta insulina -la hormona que nos engorda- para disminuir y normalizar la glucosa de la sangre. Ver ¿◊xref{que hace la insulina}?).

Sin embargo, igual que las grasas, las proteínas pueden engordarnos porque estimulan la producción de ◊xref{las incretinas}.