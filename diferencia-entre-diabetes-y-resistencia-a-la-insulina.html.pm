◊(define-meta title "diferencia entre diabetes y resistencia a la insulina")
◊hanging-topic[(topic-from-metas metas)]{En la diabetes la glucosa es más alta}

La diferencia entre la diabetes tipo dos y ◊xref{la resistencia a la insulina} es el nivel de glucosa de la sangre pues en la diabetes tipo 2, la glicemia (glucosa de la sangre) es mucho más alta. 

Por eso decimos que la ◊em{diabetes tipo dos} es un estado avanzado de ◊em{resistencia a la insulina}. O también decimos que ◊xref{la resistencia a la insulina} es un estado previo a la ◊em{diabetes}.

Lo que sucede es que en el caso de ◊xref{la resistencia a la insulina}, la glucosa en ayunas puede ser menor a ◊em{125 mg/dL}. En cambio en la diabetes, la glucosa supera este límite.

◊indented{◊image[#:width "70%" #:border #f "diabetes125.jpg"]}

Es decir, una persona puede tener resistencia a la insulina pero aún no diabetes si tiene una glucosa en sangre menor a ◊em{125 mg/dL}. Pero cuando supera este límite ya es diabética.
