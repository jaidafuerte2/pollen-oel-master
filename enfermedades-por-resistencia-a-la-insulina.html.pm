◊hanging-topic[(topic-from-metas metas)]{La resistencia a la insulina causa muchas enfermedades}

Por otro lado, tener tanta insulina permanentemente altera el funcionamiento de órganos y tejidos. Por eso la resistencia a la insulina (o también conocida como ◊em{síndrome metabólico}) causa -no sólo- obesidad y sobrepeso sino también muchas otras enfermedades peligrosas como: ◊link["diabetes-tipo-dos-y-obesidad.html"]{diabetes tipo 2}, ◊link["higado-graso-y-obesidad.html"]{hígado graso}, ◊link["presion-alta-y-obesidad.html"]{hipertensión}, ◊link["colesterol-obesidad-y-trigliceridos.html"]{colesterol y triglicéridos elevados}, entre otras. 

