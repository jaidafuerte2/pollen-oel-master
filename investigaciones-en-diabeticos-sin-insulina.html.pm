◊(define-meta title "investigaciones en diabeticos sin insulina")
◊hanging-topic[(topic-from-metas metas)]{Aumentar la insulina del cuerpo también engorda}

Además hay casos en que los pacientes no usan insulina para tratar la diabetes sino que usan fármacos que hacen que el páncreas produzca más insulina.

Este es el caso de las ◊em{sulfunilureas} que estimulan al páncreas para que produzca más insulina.

En estos pacientes se reporta que, usando sulfunilureas, aumentan más de ◊em{3 kilos} de peso.