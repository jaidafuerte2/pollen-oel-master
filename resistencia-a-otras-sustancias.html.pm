◊hanging-topic[(topic-from-metas metas)]{El exceso de sustancias que no son hormonas también puede causar resistencia}

Sin embargo, no sólo las hormonas pueden generar resistencias, el exceso de otras sustancias, como la cocaína, también generan resistencias. Sucede que cuando una persona usa esta droga por primera vez, la sensación de euforia es muy potente. Pero después se necesita más y más droga para sentir el mismo efecto.

◊indented{◊image[#:width "70%" #:border #f "resistencia-cocaina.jpg"]}

Esto sucede porque cuando una persona consume mucha cocaína muy frecuentemente, sus receptores en el cerebro se van dañando. Es decir, la cerradura dónde encaja la cocaína (en el cerebro) deja de funcionar, la droga deja de ejercer su efecto y se necesita más y más droga para sentir la misma sensación de euforia de la primera vez.