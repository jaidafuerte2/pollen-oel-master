◊hanging-topic[(topic-from-metas metas)]{El colesterol y los triglicéridos se transportan en lipoproteínas}

Recordemos que un componente fundamental de la obesidad es ◊xref{la resistencia a la insulina}, que se caracteriza por ◊em{altos niveles de insulina y glucosa}.

◊indented{◊image[#:width "70%" #:border #f "resistencia-insulina-igual.jpg"]}

◊margin-note{◊image[#:width "100%" #:border #f "higado-forma-grasas.jpg"]}

Toda esta insulina estimula al hígado para que transforme el exceso de glucosa de la sangre en grasas o triglicéridos, que se enviarán al tejido adiposo.

El problema es que los triglicéridos y las grasas -en general- no pueden viajar libremente por la circulación porque la sangre tiene mucha agua y –como sabemos- el agua y el aceite no se juntan. Es decir,  las grasas no son hidrosolubles (no se disuelven en agua). 

Por eso los triglicéridos necesitan un transportador para circular por la sangre y llegar desde el hígado al tejido adiposo. Este transportador se conoce como ◊em{lipoproteína}.

