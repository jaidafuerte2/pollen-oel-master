◊(define-meta title "estructura de las lipoproteinas")
◊hanging-topic[(topic-from-metas metas)]{Están formadas por lípidos y proteínas}

Se llaman lipoproteínas porque en su interior tienen lípidos o grasas como ◊em{colesterol y triglicéridos}. Y por fuera tienen ◊em{proteínas} que, por ser hidrosolubles, pueden viajar libremente por la sangre.

◊indented{◊image[#:width "70%" #:border #f "estructura-lipoproteina.jpg"]}

Así las lipoproteínas llevan en su interior colesterol y triglicéridos de un órgano a otro, por ejemplo, del hígado al tejido adiposo.