En cambio los ◊em{triglicéridos} son las grasas que se acumulan en el ◊em{tejido adiposo} subcutáneo o visceral y nos engordan. Pero esta grasa no se forma directamente bajo la piel o entre las vísceras sino que proviene del ◊em{hígado}.

