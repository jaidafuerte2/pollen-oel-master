◊(define-meta title "insulina y noradrenalina")
◊hanging-topic[(topic-from-metas metas)]{La noradrenalina aumenta la presión arterial}

Por ejemplo, ◊em{la insulina} estimula al ◊em{sistema nervioso simpático} para que libere mucha ◊em{noradrenalina}. La noradrenalina es una hormona de estrés que durante el paleolítico nos preparaba para luchar o huir de un animal cazador.

Lo que pasa es que la noradrenalina puede ◊em{aumentar la presión arterial y el latido cardíaco} para que llegue más sangre a los músculos que se necesitarán en una actividad física intensa como luchar o huir de un animal cazador.

Sin embargo, un evento estresante debería durar poco tiempo (sólo hasta que pase el peligro). Entonces, después de un ◊em{corto tiempo}, también debería disminuir la ◊em{noradrenalina}. (Y por eso también deberían bajar y volver a la normalidad ◊em{la frecuencia cardíaca y la presión arterial}.)

Sin embargo, en la obesidad hay mucha insulina por mucho tiempo, lo que estimula la producción de noradrenalina permanentemente, y esto  eleva la presión arterial y dificulta que vuelva a la normalidad.
