◊(define-meta title "articulaciones y cartilago")
◊hanging-topic[(topic-from-metas metas)]{El cartílago articular se desgasta y se repone constantemente}

Las ◊em{articulaciones} son la unión de dos o más huesos. Por ejemplo, en la rodilla se unen el fémur y la tibia. En esta zona de intersección, los huesos están cubiertos de ◊em{cartílago}, un tejido blanquecino que les proporciona un movimiento suave y sin fricciones.

◊indented{◊image[#:width "35%" #:border #f "cartilago-articular.jpg"]}

Además, el ◊em{cartílago}  puede soportar grandes presiones porque está formado de ◊em{colágeno} que es una proteína muy resistente pero flexible. El cartílago, también, tiene ◊em{proteoglicanos} que son proteínas que atraen agua, dándole al cartílago mayor turgencia y tolerancia a los impactos.

Normalmente el cartílago se mantiene en equilibrio, se degrada por el uso, pero se repara constantemente por unas células llamadas ◊em{condrocitos} que reponen ◊em{el colágeno y los proteoglicanos} que se pierden durante el desgaste normal.