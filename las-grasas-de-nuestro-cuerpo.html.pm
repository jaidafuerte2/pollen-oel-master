Sucede que en nuestro cuerpo hay varios tipos de ◊em{grasas o lípidos}, pero ahora nos interesan solo ◊em{los triglicéridos y el colesterol}.

El ◊em{colesterol} se encuentra en todas las ◊em{membranas} o cubiertas de las células de todos los órganos como el corazón, el cerebro, los riñones, los pulmones, los intestinos, etc.

