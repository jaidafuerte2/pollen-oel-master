◊hanging-topic[(topic-from-metas metas)]{La insulina retiene agua y sodio lo que sube la presión}

Por otro lado, el ◊em{exceso de insulina} (de las personas obesas) también eleva la presión reteniendo ◊em{sodio en el riñón}.  El sodio es parte fundamental del ◊em{cloruro de sodio (o más conocido como sal de mesa)} y tiene la capacidad de atraer agua. 

◊indented{◊image[#:width "70%" #:border #f "sodio-renal.jpg"]}

Por eso se dice que “◊em{el agua sigue al sodio como la sombra al cuerpo}”.

El problema es que las personas obesas tienen niveles muy altos de ◊link["que-hace-la-insulina.html"]{insulina} (la hormona que aumenta nuestras reservas de grasa) y esta hormona estimula al riñón para que retenga sodio y también agua, lo que aumenta la ◊em{presión arterial}.
Como cuando por unas tuberías circula demasiada agua.

Además el exceso de tejido graso produce ◊em{angiotensina}. La ◊em{angiotensiona} es una sustancia que también retiene sodio y agua en el riñón, lo que aumenta la presión.
