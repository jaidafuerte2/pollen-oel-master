◊(define-meta title "por que engordan los edulcorantes")
◊hanging-topic[(topic-from-metas metas)]{Porque estimulan la producción de insulina}

Esto sucede porque –aunque tienen pocas calorías- los edulcorantes si estimulan la ◊em{producción de insulina} y esto nos engorda (Ver ¿◊xref{que hace la insulina}). Por ejemplo la sucralosa eleva la insulina un 20%.  Mientras que la estevia y el aspartamo elevan la insulina, incluso, más que el azúcar.

De hecho, el doctor David Ludwig, de la Universidad de Harvard, realizó una investigación en adolescentes a los que dividió en dos grupos:

◊numbered-list{
Un grupo recibió bebidas light con ◊em{edulcorante} para saciar la sed y


Otro grupo siguió con sus bebidas habituales con ◊em{azúcar}.}

Al final del estudio, luego de dos años de seguimiento, concluyó que los participantes del grupo que usaron ◊em{bebidas light} no adelgazaron más que los participantes del grupo que seguía tomando ◊em{bebidas azucaradas}.