◊(define-meta title "lesiones de las arterias")
◊hanging-topic[(topic-from-metas metas)]{Normalmente las arterias se lesionan desde la infancia}

Lo que pasa es que el propio flujo de la sangre genera ◊em{lesiones en las arterias} desde la infancia, que se diseminan y desarrollan en la edad avanzada.

Normalmente estas ◊em{lesiones} inician una ◊em{respuesta inflamatoria moderada} con pocas citoquinas y pocos glóbulos blancos que ayudarán a que una ◊em{arteria lesionada} se regenere. (Ver ¿Qué es ◊xref{la inflamacion}?)

Además, como las membranas de todas las células tienen ◊link["las-grasas-de-nuestro-cuerpo.html"]{◊em{colesterol}}, un poco de ◊link["las-grasas-de-nuestro-cuerpo.html"]{◊em{colesterol}} debe llegar a la zona dañada de una arteria para que se regeneren sus células.

◊indented{◊image[#:width "70%" #:border #f "membrana-colesterol.jpg"]}

Así que, en una persona saludable, las lesiones que sufren las arterias generan una ◊em{respuesta inflamatoria moderada} que sana fácil y rápido (con pequeñas cantidades de citoquinas, glóbulos blancos y ◊link["las-grasas-de-nuestro-cuerpo.html"]{◊em{colesterol}}).

