◊margin-note{◊image[#:width "85%" #:border #f "aceite-de-oliva.jpg"]}

Grasas naturales muy saludables son ◊em{el aceite de oliva, el aguacate, la mantequilla, el sebo de ternera y la manteca de cerdo}.  Sobre todo el aceite de oliva tiene propiedades antiinflamatorias, reduce el colesterol, la presión arterial y el riesgo de padecer infartos.