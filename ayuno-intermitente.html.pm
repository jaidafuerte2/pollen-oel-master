◊margin-note{Ayunar es todo período de tiempo en el que no comemos alimentos}

◊indented{◊image[#:width "70%" #:border #f "ayuno-intermitente.jpg"]}

Todo lo que explicamos, sobre ¿◊xref{que debemos comer para adelgazar}?, es muy adecuado si queremos bajar de peso. Sin embargo, hay algo que es más importante que el tipo de comida. De hecho, para adelgazar y tener una buena salud, es más importante ◊em{la frecuencia} con la que comemos.

Es decir, los períodos que no comemos alimentos (o de ◊em{ayuno}) son muy importantes para bajar de peso y tener buena salud.
