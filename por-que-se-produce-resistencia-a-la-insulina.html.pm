◊hanging-topic[(topic-from-metas metas)]{Porque el cuerpo empieza a producir demasiada insulina}

Pero ¿Por qué se daña esta cerradura en las personas con resistencia a la insulina?

Lo que pasa es que una resistencia es un mecanismo de defensa de los seres vivos frente al exceso de cualquier hormona, no sólo de insulina.

Esto sucede porque el cuerpo debe producir cantidades muy precisas de estas sustancias que no deben excederse en cantidad ni en frecuencia porque esto nos enfermaría. Por eso nuestras hormonas –normalmente- no se liberan en grandes cantidades todo el tiempo sino sólo en ciertos momentos.