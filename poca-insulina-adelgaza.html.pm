◊(define-meta title "poca insulina adelgaza")
◊hanging-topic[(topic-from-metas metas)]{}

Así que, mucha insulina si engorda. En cambio hay fármacos que disminuyen la glucosa y como respuesta, el páncreas produce menos insulina pues ya no es necesario sacar el exceso de glucosa de la sangre. Y poca insulina  nos hace adelgazar.

Por ejemplo, ◊em{la metformina} es un fármaco que disminuye la producción de glucosa en el hígado. Además hay medicamentos que interfieren la absorción de glucosa en el intestino o aumentan su salida por la orina. Al final, el efecto de estos fármacos es ◊em{reducir la glucosa de la sangre y también la insulina}. Lo que nos hace adelgazar.

Además, hay enfermedades en las que el páncreas produce poco o nada de insulina. Este es el caso de la ◊em{diabetes tipo 1} que se caracteriza por una marcada reducción de peso.
