◊(define-meta title "otros beneficios de la fibra")
◊hanging-topic[(topic-from-metas metas)]{que nos ayudan a adelgazar}

Además, como la fibra retiene agua, puede aumentar el volumen de las heces en el intestino grueso y así ◊em{aumenta la excreción de calorías}, lo que nos ayuda a adelgazar.

En cambio comer poca fibra causa ◊em{estreñimiento}.

◊margin-note{◊image[#:width "100%" #:border #f "constipacion.jpg"]}

Además, como los carbohidratos con fibra retienen agua y tienen un mayor volumen, se movilizan más lento del estómago al intestino y su absorción es más lenta. Así, ◊em{la glucosa de la sangre no sube tan rápido} y el páncreas produce menos insulina (lo que nos ayuda a  adelgazar. Ver ¿◊xref{que hace la insulina}?).
