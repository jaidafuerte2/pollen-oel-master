◊hanging-topic[(topic-from-metas metas)]{tiene varios beneficios que nos ayudan a adelgazar}

La ◊em{fibra} es la parte no digerible de un carbohidrato como ◊em{la cáscara de las frutas o  la cáscara de las legumbres} como el fréjol y las habas o la cáscara de los granos enteros como el trigo.

◊indented{◊image[#:width "70%" #:border #f "legumbres.jpg"]}

La ◊em{fibra} es una especie de antídoto (contra los efectos engordadores de los carbohidratos) pues tiene varios beneficios.

Uno de estos beneficios es que disminuye el apetito porque la fibra absorbe agua y aumenta su volumen, lo que ayuda a llenar el estómago e incrementa la sensación de saciedad. 

◊margin-note{◊image[#:width "85%" #:border #f "naranjas-dibujo.jpg"]}

Por eso es fácil tomar un jugo de cinco naranjas. Pero es más difícil comer las cinco naranjas enteras (porque la fruta entera si tiene fibra y el jugo no).
