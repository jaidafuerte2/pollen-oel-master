◊hanging-topic[(topic-from-metas metas)]{La diabetes daña órganos y tejidos}

Esto es un problema porque tanta glucosa en la sangre es tóxico para el organismo y puede dañar órganos y tejidos. Un ejemplo puede ser el ◊em{daño de los riñones} que es muy frecuente en la diabetes. 

◊margin-note{◊image[#:width "50%" #:border #f "ceguera.jpg"]}

O también, el ◊em{daño de la retina} que produce ◊em{ceguera} y también es frecuente en diabéticos.  