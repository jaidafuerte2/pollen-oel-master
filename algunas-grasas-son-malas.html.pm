Además hay grasas que si son muy tóxicas y debemos evitarlas casi por completo. Este es el caso de las ◊em{grasas trans saturadas artificialmente} presentes en ◊em{la manteca vegetal y la margarina}.

◊indented{◊image[#:width "60%" #:border #f "margarina-grasa-mala.jpg"]}

Estas ◊em{grasas trans}, en su composición química, están saturadas o repletas de hídrógeno. Y son ◊em{artificiales} porque se crean artificialmente, introduciendo hidrógeno en el ◊em{aceite vegetal}, transformando a las ◊xref{grasas con omega seis} (no saturadas) en grasas saturadas artificialmente.

Las ◊em{grasas trans}, con este cambio químico, se vuelven más estables y duraderas. Por eso este tipo de grasas ayudan a conservar alimentos como pasteles, frituras, snacks y galletas en funda. Además son las grasas preferidas por varias cadenas de comida rápida.

◊indented{◊image[#:width "60%" #:border #f "pollo-frito.jpg"]}

Por ser de origen vegetal, se creía que las ◊em{grasas trans} eran buenas para la salud. Pero estudios al respecto, concluyeron que estas grasas trans saturadas artificialmente aumentan el riesgo de ◊em{enfermedades cardiovasculares}.
